package calc

// Adds two numbers.
// Returns the result.
func Add(a, b int) int {
	return a+b
}

// Subtracts two numbers.
// Returns the result.
func Subtract(a, b int) int {
	return a-b
}

// Multiples two numbers.
// Returns the result.
func Multiply(a, b int) int {
	return a*b
}

// Divides two numbers.
// Returns the result.
func Divide(a, b int) float64 {
	return float64(a/b)
}