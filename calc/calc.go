package calc

// Adds two numbers.
// Returns the result.
func Add(a, b int64) int64 {
	return a+b
}

// Substracts two numbers.
// Returns the result.
func Substract(a, b int64) int64 {
	return a-b
}

// Multiples two numbers.
// Returns the result.
func Multiply(a, b int64) int64 {
	return a*b
}

// Divides two numbers.
// Returns the result.
func Divide(a, b int64) float64 {
	return float64(a/b)
}